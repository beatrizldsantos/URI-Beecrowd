package URI_JAVA;
import java.util.Scanner;

public class URI1197{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (sc.hasNext()) {
            int v = sc.nextInt();
            int t = sc.nextInt();
            System.out.println(2 * v * t);
        }
    }
}
